class MinStack {
public:
    MinStack() {
        
    }
    
    void push(int val) {
		if (st.empty()) {
			st.push_back({val, val});
			return;
		}
		st.push_back({val, min(val, st.back().second)});
    }
    
    void pop() {
		st.pop_back();
    }
    
    int top() {
		return st.back().first;
    }
    
    int getMin() {
		return st.back().second;
    }

private:
	vector<pair<int, int>> st;
};

void solve(){
	MinStack s;
	s.push(-2);
	s.push(0);
	s.push(-3);
	cout << s.getMin() << '\n';
	s.pop();
	cout << s.top() << '\n';
	cout << s.getMin() << '\n';
}

